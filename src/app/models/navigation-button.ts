/**
 * Contains possible buttons that correspond to Navigation Panel modes.
 */
export enum NavigationButton {
  /**
   * User invoked the functionality that will show the Navigation Form Component.
   */
  Add,
  /**
   * User started the DeleteMode in Sidebar Navigation Component.
   */
  Delete,
  /**
   * User started DragMode in Sidebar Navigation Component.
   */
  Drag
}
